import React, { useState } from 'react';
import './ProductImage.css';

const ProductImage = ({ src, alt, className = '', fallbackSrc = '/images/products/tomatoes.jpeg' }) => {
  const [imgSrc, setImgSrc] = useState(src);
  const [hasError, setHasError] = useState(false);

  const handleError = () => {
    if (!hasError) {
      setHasError(true);
      setImgSrc(fallbackSrc);
    }
  };

  return (
    <img
      src={imgSrc}
      alt={alt}
      className={className}
      onError={handleError}
      loading="lazy"
    />
  );
};

export default ProductImage;
